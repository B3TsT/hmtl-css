/**
 * *Detectar si una letra es vocal o no
 */
let v = 'h', print = '';

if (v === 'a' || v === 'A') {
  print = `${v} es VOCAL`;
} else if (v === 'e' || v === 'E') {
  print = `${v} es VOCAL`;
} else if (v === 'i' || v === 'I') {
  print = `${v} es VOCAL`;
} else if (v === 'o' || v === 'O') {
  print = `${v} es VOCAL`;
} else if (v === 'u' || v === 'U') {
  print = `${v} es VOCAL`;
} else {
  print = `${v} es no es VOCAL`;
}

console.log(print)
